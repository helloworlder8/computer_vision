# Detect_SMART
class Detect_SMART(nn.Module):
    """YOLOv8 Detect head for detection models."""

    dynamic = False  # force grid reconstruction
    export = False  # export mode
    shape = None
    anchors = torch.empty(0)  # init
    strides = torch.empty(0)  # init

    def __init__(self, num_class=80, channel_list=()):
        """Initializes the YOLOv8 detection layer with specified number of classes and channels."""
        super().__init__()
        self.num_class = num_class  # 类别数 4 
        self.num_layer = len(channel_list)  # 检测层数 3
        self.reg_max = 16  # DFL channels (channel_list[0] // 16 to scale 4/8/12/16/20 for n/s/m/l/x)
        self.num_outchannel = num_class + self.reg_max * 4  # 输出通道数 68
        self.stride = torch.zeros(self.num_layer)  # 步长
        mid_channel_regression, mid_channel_class = max((16, channel_list[0] // 4, self.reg_max * 4)), max(channel_list[0]//4, min(self.num_class, 100))  # channels 64 56
# def __init__(self, in_channels, out_channels, split_ratio=0.2): 
# def __init__(self, in_channels, out_channels, n=1, split_ratio=0.2, stride=1, conv_or_identity=0,conv_poolconv_pool=0,right_bottleneckratio=1):   
        self.cv2 = nn.ModuleList(
            nn.Sequential(ASSNET2(x, mid_channel_regression, 0.6), ASSNET2(mid_channel_regression, mid_channel_regression, 0.8), 
                          nn.Conv2d(mid_channel_regression, 4 * self.reg_max, 1)) for x in channel_list
        )
        self.cv3 = nn.ModuleList(nn.Sequential(ASSNET2(x, mid_channel_class, 0.18), ASSNET2(mid_channel_class, mid_channel_class, 0.8), 
                                nn.Conv2d(mid_channel_class, self.num_class, 1)) for x in channel_list)
        self.dfl = DFL(self.reg_max) if self.reg_max > 1 else nn.Identity()

    def forward(self, x):
        """Concatenates and returns predicted bounding boxes and class probabilities."""
        for i in range(self.num_layer):
            x[i] = torch.cat((self.cv2[i](x[i]), self.cv3[i](x[i])), 1)
        if self.training:  # Training path
            return x

        # Inference path
        shape = x[0].shape  # BCHW
        x_cat = torch.cat([xi.view(shape[0], self.num_outchannel, -1) for xi in x], 2)
        if self.dynamic or self.shape != shape:
            self.anchors, self.strides = (x.transpose(0, 1) for x in make_anchors(x, self.stride, 0.5))
            self.shape = shape

        if self.export and self.format in ("saved_model", "pb", "tflite", "edgetpu", "tfjs"):  # avoid TF FlexSplitV ops
            box = x_cat[:, : self.reg_max * 4]
            cls = x_cat[:, self.reg_max * 4 :]
        else:
            box, cls = x_cat.split((self.reg_max * 4, self.num_class), 1)

        if self.export and self.format in ("tflite", "edgetpu"):
            # Precompute normalization factor to increase numerical stability
            # See https://github.com/ultralytics/ultralytics/issues/7371
            grid_h = shape[2]
            grid_w = shape[3]
            grid_size = torch.tensor([grid_w, grid_h, grid_w, grid_h], device=box.device).reshape(1, 4, 1)
            norm = self.strides / (self.stride[0] * grid_size)
            dbox = self.decode_bboxes(self.dfl(box) * norm, self.anchors.unsqueeze(0) * norm[:, :2])
        else:
            dbox = self.decode_bboxes(self.dfl(box), self.anchors.unsqueeze(0)) * self.strides

        y = torch.cat((dbox, cls.sigmoid()), 1)
        return y if self.export else (y, x)

    def bias_init(self):
        """Initialize Detect() biases, WARNING: requires stride availability."""
        m = self  # self.model[-1]  # Detect() module
        # cf = torch.bincount(torch.tensor(np.concatenate(dataset.labels, 0)[:, 0]).long(), minlength=num_class) + 1
        # ncf = math.log(0.6 / (m.num_class - 0.999999)) if cf is None else torch.log(cf / cf.sum())  # nominal class frequency
        for a, b, s in zip(m.cv2, m.cv3, m.stride):  # from
            a[-1].bias.data[:] = 1.0  # box
            b[-1].bias.data[: m.num_class] = math.log(5 / m.num_class / (640 / s) ** 2)  # cls (.01 objects, 80 classes, 640 img)

    def decode_bboxes(self, bboxes, anchors):
        """Decode bounding boxes."""
        return dist2bbox(bboxes, anchors, xywh=True, dim=1)
    




